package com.huimaibao.app.fragment.home.entity;

import java.io.Serializable;

/** 
 * ITEM的对应可序化队列属性
 */
public class IncomeListEntity implements Serializable {

	public String IncomeId;
	public String IncomeName;
	public String IncomeNum;
	public String IncomeImage;
	public String IncomeUrl;
	public String IncomeTime;
	public String IncomeMoney;

	public String getIncomeId() {
		return IncomeId;
	}

	public void setIncomeId(String IncomeId) {
		this.IncomeId = IncomeId;
	}

	public String getIncomeName() {
		return IncomeName;
	}

	public void setIncomeName(String IncomeName) {
		this.IncomeName = IncomeName;
	}

	public String getIncomeNum() {
		return IncomeNum;
	}

	public void setIncomeNum(String IncomeNum) {
		this.IncomeNum = IncomeNum;
	}

	public String getIncomeImage() {
		return IncomeImage;
	}

	public void setIncomeImage(String IncomeImage) {
		this.IncomeImage = IncomeImage;
	}

	public String getIncomeUrl() {
		return IncomeUrl;
	}

	public void setIncomeUrl(String IncomeUrl) {
		this.IncomeUrl = IncomeUrl;
	}

	public String getIncomeTime() {
		return IncomeTime;
	}

	public void setIncomeTime(String IncomeTime) {
		this.IncomeTime = IncomeTime;
	}

	public String getIncomeMoney() {
		return IncomeMoney;
	}

	public void setIncomeMoney(String IncomeMoney) {
		this.IncomeMoney = IncomeMoney;
	}






}